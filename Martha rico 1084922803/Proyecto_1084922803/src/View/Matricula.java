    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Integer numPerio;
        String perdiodo;
        Integer idEs;
        Integer idMa;
        
        numPerio = Integer.parseInt(txtNumPeriodo.getText()) ;
        perdiodo = txtPeriodo.getText();
        idEs = Integer.parseInt(txtIdEstu.getText());
        idMa = Integer.parseInt(txtIdMateria.getText());
        
        MatriculaC ec = new MatriculaC();
        ec.Agregar(numPerio, perdiodo, idEs, idMa);
    }//GEN-LAST:event_btnGuardarActionPerformed

