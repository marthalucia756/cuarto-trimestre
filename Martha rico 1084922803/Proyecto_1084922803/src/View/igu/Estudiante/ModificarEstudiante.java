    /**
     * @param args the command line arguments
     */
    public void mostrar() {
        String sql = "select * from estudiante";
        try {
            conn.getConnection();
            rs = conn.consultarSQL(sql);
            Object[] estado = new Object[6];
            model = (DefaultTableModel) tblEstado.getModel();
            while (rs.next()) {                
                estado[0] = rs.getInt("id");
                estado[1] = rs.getString("nombre");
                estado[2] = rs.getString("apellido");
                estado[3] = rs.getInt("grado");
                estado[4] = rs.getInt("num_identificacion");
                estado[5] = rs.getInt("edad");
                
                model.addRow(estado);
            }
            tblEstado.setModel(model);
        } catch (Exception e) {
        }

    }

